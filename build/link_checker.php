 * @param string $url The url to check
 */
function find_user_sesskey($url) {
    global $USER_SESSKEY;

    // split query params into an array
    $query = parse_url($url, PHP_URL_QUERY);
    $query_parts = array();
    parse_str($query, $query_parts);

    foreach ($query_parts as $key => $value) {
        if ($key == 'sesskey') {
            $USER_SESSKEY = $value;
            break;
        }
    }
}
